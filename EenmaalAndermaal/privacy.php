    <h3>Privacybeleid</h3>
    <br>
    <br>
    <h4>1. Algemeen</h4>
    <br>
    <p>Dit Privacybeleid beschrijft het beleid van iConcepts met betrekking tot het verzamelen, gebruiken, opslaan,
        delen en beschermen van uw persoonsgegevens. Dit Privacybeleid is van toepassing op de website
        EenmaalAndermaal.nl en alle gerelateerde websites, applicaties, diensten en tools waarin naar dit beleid wordt
        verwezen, ongeacht de wijze waarop u toegang heeft verkregen, inclusief toegang of gebruik met behulp van
        mobiele apparatuur. iConcepts is de verantwoordelijke voor de gegevensverwerking in de zin van de Wet
        Bescherming Persoonsgegevens.
        <br><br>
        Reikwijdte en toestemming: Door gebruik te maken van EenmaalAndermaal.nl en daaraan gerelateerde Diensten, stemt
        u uitdrukkelijk in met het verzamelen, gebruiken, bekendmaken en bewaren door ons van uw persoonsgegevens, zoals
        beschreven in dit Privacybeleid en onze Gebruiksvoorwaarden.
        iConcepts kan dit Privacybeleid van tijd tot tijd wijzigen. Wij raden u dan ook aan deze regelmatig te lezen.
        Substantiële wijzigingen in ons Privacybeleid zullen wij mededelen op onze Website. Het gewijzigde Privacybeleid
        zal onmiddellijk van kracht zijn nadat deze voor het eerst op onze Website is geplaatst. Dit Privacybeleid is
        van kracht vanaf 23-04-2018.</p>
    <br>
    <h4>2. Welke persoonsgegevens verzamelen wij</h4>
    <br>
    <p>U kunt onze Website bezoeken zonder u te hoeven aanmelden door middel van het registreren van een account.
        Wanneer u besluit om ons uw persoonsgegevens te verstrekken, stemt u ermee in dat die gegevens worden verzonden
        naar en opgeslagen worden op onze servers. "Persoonsgegevens" zijn gegevens die aan een specifieke persoon
        kunnen worden gekoppeld en bruikbaar zijn om een specifieke persoon te identificeren. Indien gebruikersgegevens
        zijn geanonimiseerd, of geaggregeerd dan kunnen deze gegevens niet gebruikt worden om een persoon te
        identificeren en zijn hierdoor niet te classificeren als persoonsgegevens. Wij verzamelen de volgende soorten
        persoonsgegevens:
        Gegevens die wij automatisch verzamelen: Wanneer u onze websites bezoekt, onze applicaties, Diensten en tools
        gebruikt of op advertenties of overige content reageert, verzamelen wij automatisch de gegevens die naar ons
        worden gestuurd door uw computer, mobiele apparatuur of ander apparatuur waarmee u toegang heeft. Deze gegevens
        omvatten maar zijn niet beperkt tot:
        <br><br>
        • de unieke identificatie van het toestel of mobiele apparaat, soort toestel of apparaat, informatie over uw
        geografische locatie, informatie over computer en verbinding, statistieken betreffende bekeken pagina’s, verkeer
        van en naar EenmaalAndermaal.nl, verwijzende URL, advertentiegegevens, en IP-adres; en<br>
        • informatie die wij verzamelen met behulp van cookies en webbeacons.
        <br><br>
        Gegevens die u aan ons verstrekt: Wij verzamelen en bewaren alle gegevens die u op onze websites invoert of die
        u aan ons verstrekt wanneer u gebruik maakt van onze Diensten. Deze gegevens omvatten, maar zijn niet beperkt
        tot:
        <br><br>
        • gegevens die u aan ons verstrekt wanneer u zich aanmeldt voor een account of voor de Diensten die u
        afneemt;<br>
        • aanvullende gegevens die u mogelijk aan ons verstrekt via social media websites of Diensten van derden;<br>
        • andere gegevens die u genereert, of die horen bij uw account en uw gebruik van de website, applicaties,
        diensten en tools (zoals gebruik van functionaliteit om een advertentie te plaatsen, zoekopdrachten, bekeken en
        bewaarde advertenties of uw gebruik van de contactmogelijkheden bij een advertentie), daaronder begrepen
        gepseudonymiseerde informatie met betrekking tot het bellen van een aan u in een advertentie getoond
        telefoonnummer;<br>
        • gegevens die worden verstrekt in het kader van geschillenbeslechting, correspondentie via onze websites of
        correspondentie die ons wordt toegezonden; en <br>
        • gegevens over uw locatie en de locatie van uw toestel, inclusief unieke identificatiegegevens van uw toestel
        indien u deze dienst heeft ingeschakeld op uw mobiele apparaat.
        <br><br>
        Gegevens uit andere bronnen: Wij kunnen aanvullende gegevens over u ontvangen of verzamelen van derden en deze
        toevoegen aan onze accountinformatie. Deze gegevens omvatten, maar zijn niet beperkt tot: demografische
        gegevens, navigatiegegevens, aanvullende contactgegevens en aanvullende gegevens over u uit andere bronnen,
        zoals overheidsinstanties, voor zover toegestaan door de wet.
        Naar aanleiding van meldingen, al dan niet via het Meldpunt Internetoplichting van de Politie, kunnen wij
        informatie (waaronder persoonsgegevens) verwerken over bepaalde geschillen tussen gebruikers en adverteerders,
        onder meer door deze gegevens op te slaan in de Transactiegeschillen Database ("TGD") van iConcepts. De gegevens
        die iConcepts in dit kader verwerkt worden niet op grond van eigen onderzoek door IConcepts vastgelegd, maar
        zijn afkomstig van gebruikers van IConcepts die door middel van een melding informatie aan IConcepts of aan het
        Meldpunt Internetoplichting verstrekken en/of betreffen reeds aanwezige informatie op de IConcepts website.</p>
