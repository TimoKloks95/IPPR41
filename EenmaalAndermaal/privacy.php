    <h4>5. Toegang tot, bekijken en aanpassen van uw persoonsgegevens</h4>
    <br>
    <p>Indien u uw account bij ons wilt sluiten, dan kunt u ons daartoe een verzoek sturen. Wij zullen uw verzoek
        verwerken binnen een redelijke termijn en uw persoonsgegevens verwerken in overeenstemming met de toepasselijke
        wetgeving. Anderzijds kunt u ook contact opnemen met Customer Support voor het inzien van uw persoonsgegevens
        die wij opslaan en die niet direct raadpleegbaar zijn op IConcepts, tenzij IConcepts op grond van de Wet
        bescherming persoonsgegevens niet gehouden is deze inzage te verschaffen. Om aan uw verzoek te mogen voldoen,
        hebben we het volgende van u nodig:
        <br><br>
        • Een ondertekende versie van uw verzoek waarin uw contact gegevens en uw e-mailadres vermeld is
        • Een kopie van uw geldige legitimatiebewijs (na controle zal IConcepts deze vernietigen)
        <br><br>
        Wij kunnen hiervoor een vergoeding van kosten in rekening brengen; dit bedrag zal de wettelijke toegestane
        bedragen niet overschrijden. Indien uw gegevens feitelijk onjuist zijn dan wel onvolledig zijn of niet ter zake
        doen voor de doeleinden waarvoor wij uw gegevens verwerken, dan kunt u ons verzoeken om uw gegevens te wijzigen
        of te verwijderen. Dergelijke verzoeken zullen in overeenstemming met de Wet Bescherming Persoonsgegevens worden
        behandeld.</p>
    <br>
    <h4>6. Bescherming en opslag van uw persoonsgegevens</h4>
    <br>
    <p>Wij beschermen uw gegevens met behulp van technische en administratieve veiligheidsmaatregelen om het risico van
        verlies, misbruik, onbevoegde toegang, openbaarmaking en wijziging zo klein mogelijk te maken. Hierbij kunt u
        denken aan firewalls, versleuteling van gegevens, en fysieke en administratieve toegangscontroles tot aan de
        data en servers.
        Wij bewaren persoonsgegevens niet langer dan wettelijk is toegestaan en verwijderen persoonsgegevens wanneer
        deze niet langer noodzakelijk zijn voor de hierboven beschreven doeleinden.</p>
