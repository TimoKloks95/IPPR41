    <h4>3. Hoe gebruiken wij uw persoonsgegevens</h4>
    <br>
    <p>U stemt ermee in dat wij uw verzamelde persoonsgegevens (zie hierboven) kunnen gebruiken voor de volgende
        doeleinden:
        <br><br>
        • om u toegang te geven tot onze Diensten en klantenondersteuning te leveren door middel van email of per
        telefoon; <br>
        • om mogelijk verboden of illegale activiteiten, fraude en inbreuken op de beveiliging te voorkomen, op te
        sporen en te onderzoeken, en onze Gebruiksvoorwaarden te handhaven (denk hierbij aan het een halt toeroepen van
        verdachte conversaties via onze website);<br>
        • om onze Diensten, content en advertenties op u af te stemmen, te meten en te verbeteren;<br>
        • om contact met u op te nemen, via e-mail, push notificatie, tekstbericht (SMS) of telefonisch, om u te
        informeren over onze Diensten en de diensten van ondernemingen die onderdeel uitmaken van de eBay Inc.
        ‘corporate family’ (hierna te noemen ‘eBay Groep’), ten behoeve van gerichte marketingactiviteiten, updates en
        promotionele aanbiedingen, gebaseerd op uw berichtvoorkeuren, of voor enige andere doeleinden voor zover
        beschreven in dit Privacybeleid; en<br>
        • om u andere diensten te verlenen waarom u heeft verzocht, als beschreven toen wij de gegevens verzamelden.
        <br><br>
        Gegevens delen met en registratie op social media sites: Wij kunnen sign-on Diensten aanbieden die het u
        mogelijk maken om met behulp van uw inloggegevens op andere websites toegang te krijgen tot de website
        IConcepts.nl of gerelateerde websites. We kunnen ook Diensten aanbieden die het u mogelijk maken om gegevens te
        delen met social media websites van derden, zoals Facebook, Google Plus en Twitter.
        Indien u ons toegang geeft tot bepaalde persoonsgegevens die zijn opgeslagen op websites van derden, zal de
        reikwijdte van deze toegang tot persoonsgegevens verschillen per website en zal deze worden bepaald door uw
        eigen browser instellingen evenals door uw toestemming. Indien u uw account bij een derde partij wenst te
        verbinden aan uw IConcepts account en u ons toestemming geeft tot deze gegevens, stemt u ermee in dat wij
        gegevens van deze derde websites kunnen verzamelen, gebruiken en opslaan in overeenstemming met dit
        Privacybeleid.
        Doorgifte van uw persoonsgegevens aan derden: Wij kunnen uw persoonsgegevens bekend maken aan derden in
        overeenstemming met dit Privacybeleid en de toepasselijke wet- en regelgeving. Zoals verder omschreven onder 4,
        maken wij uw persoonsgegevens niet bekend aan derden voor hun marketingdoeleinden zonder uw uitdrukkelijke
        toestemming. Wij kunnen uw persoonsgegevens delen met:
        <br><br>
        • dienstverleners met wie wij een contract hebben gesloten om ons te helpen met het aanbieden van onze Diensten
        op de website (denk hierbij aan financiële dienstverlening, marketing bureaus en technische ondersteuning). In
        dergelijke gevallen blijven de persoonsgegevens onder de zeggenschap van EenmaalAndermaal.nl.<br>
        • bepaalde derden (zoals rechthebbenden van intellectuele eigendomsrechten, toezichthouders, belastingdienst,
        politie en andere wettelijke instanties), indien wij daartoe verplicht zijn op grond van wet- en regelgeving of
        aanleiding zien op grond van dit Privacybeleid. Wij kunnen uw persoonlijk gegevens delen:
        <br><br>
        - om te voldoen aan wettelijke verplichtingen of een rechterlijk bevel; of<br>
        - indien dit noodzakelijk is ter voorkoming, opsporing of vervolging van strafbare feiten (zoals fraude, bedrog
        of vervolging); of<br>
        - indien het noodzakelijk is voor het handhaven van ons beleid of ter bescherming van de rechten en vrijheden
        van anderen.<br><br>
        • Adverteerders Indien u via de Website interesse heeft getoond in de aanschaf van een in de Advertentie
        aangeboden product en/of dienst of via de Website reageert op een Advertentie, om verdere communicatie en/of de
        eventuele transactie (koop/verkoop) met betrekking tot het in de Advertentie aangeboden product en/of dienst
        tussen u en de Adverteerder mogelijk te maken.<br>
        • andere derden waaraan u toestemming heeft gegeven om uw gegevens te delen met EenmaalAndermaal.nl bijvoorbeeld
        in het kader van een samenwerking).<br>
        • ondernemingen waarmee wij in het kader van een reorganisatie van plan zijn te fuseren of waardoor wij worden
        overgenomen.<br>
        • leden van ons Melding van Inbreuk Programma, indien de eigenaar van een intellectueel eigendomsrecht of een
        tussenpersoon te goeder trouw van oordeel is dat een advertentie de rechten van de eigenaar schenden. Met alle
        deelnemers aan het ‘Melding van Inbreuk Programma’ zal, voordat persoonsgegevens worden verstrekt, een
        overeenkomst worden gesloten die onder meer inhoudt dat gegevens worden verstrekt onder de strikte voorwaarde
