    <h4>4. Marketing doeleinden</h4>
    <br>
    <p>U gaat ermee akkoord dat wij de door ons verzamelde gegevens mogen gebruiken om u, al dan niet gepersonaliseerde,
        aanbiedingen te sturen of telefonisch contact met u op te nemen voor producten of Diensten van IConcepts of
        ondernemingen van de eBay Groep die interessant voor u zouden kunnen zijn.
        Wij verkopen of verhuren uw persoonsgegevens niet aan derden voor hun marketingdoeleinden zonder uw
        uitdrukkelijke toestemming. Wij kunnen uw gegevens combineren met gegevens die wij van andere bedrijven
        verzamelen en deze gebruiken om onze Diensten en functionaliteit te verbeteren en te personaliseren.</p>
