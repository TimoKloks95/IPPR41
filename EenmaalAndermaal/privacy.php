        juridisch advies en/of voor het beantwoorden van vragen van de adverteerder in kwestie.<br>
        Zonder het voorgaande te beperken, zullen wij - in ons streven om uw privacy te respecteren en de website vrij
        te houden van kwaadwillende personen of partijen - uw persoonsgegevens overigens niet bekend maken aan
        rechtshandhavers, andere overheidsfunctionarissen of andere derden zonder gerechtelijk bevel of formeel verzoek
        van de overheid in overeenstemming met geldende wetgeving, tenzij wij te goeder trouw menen dat de bekendmaking
        van informatie noodzakelijk is om een dreigend letsel of dreigende financiële schade te voorkomen of om
        vermeende illegale activiteiten te melden.
        Gegevens die u deelt op IConcepts.nl: Op onze website hebben gebruikers de mogelijkheid om advertenties en
        andere gegevens met andere gebruikers te delen, waardoor deze gedeelde informatie voor andere gebruikers
        toegankelijk wordt. Aangezien u op onze website ook in staat wordt gesteld om direct in contact te komen met een
        koper of verkoper, raden wij u aan om na te denken over de manier waarop u uw persoonsgegevens met anderen
        deelt. U bent zelf verantwoordelijk voor de persoonsgegevens die u deelt via onze website en daardoor kunnen wij
        de privacy of veiligheid van de door u gedeelde gegevens met andere gebruikers dan ook niet garanderen.
        <br><br>
        Wij raden u sterk aan om, indien u onze website bezoekt vanaf een gedeelde computer of een computer in een
        internetcafé, na elke sessie uit te loggen. Wanneer u niet wilt dat de gedeelde computer u en/of uw gegevens
        onthoudt, dan zult u de cookies en/of de geschiedenis van uw website bezoek moeten verwijderen.</p>
